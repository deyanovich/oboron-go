package obcrypt

// hardcodedKey is the 64-byte HARDCODED_KEY_BYTES shared with the Rust
// reference (oboron-rs) for cross-language keyless interoperability. It is
// public and therefore provides NO security — it exists only for testing and
// for the CLI's --keyless mode.
//
// Hex: 381284633d02ea5f...a95dbdd5 (see HardcodedKeyBytes for the full value).
var hardcodedKey = [KeySize]byte{
	0x38, 0x12, 0x84, 0x63, 0x3d, 0x02, 0xea, 0x5f, 0x35, 0xdf, 0x85, 0x96, 0xb5, 0xcc, 0x42, 0x18,
	0x31, 0x00, 0x60, 0x46, 0x8e, 0x8b, 0x46, 0x54, 0x55, 0xa4, 0x15, 0x17, 0x4e, 0xa6, 0xe9, 0x66,
	0xa9, 0xf4, 0x8e, 0xec, 0x4b, 0xa4, 0x46, 0xdd, 0xfc, 0x8b, 0x78, 0x58, 0x78, 0x95, 0x35, 0x6f,
	0x45, 0xa7, 0x5a, 0x1a, 0xb7, 0x41, 0x94, 0x54, 0xdd, 0x9f, 0x7a, 0xa8, 0xa9, 0x5d, 0xbd, 0xd5,
}

// HardcodedKeyBytes returns a copy of the public, hardcoded 64-byte key.
// Testing only — NOT SECURE.
func HardcodedKeyBytes() []byte {
	out := make([]byte, KeySize)
	copy(out, hardcodedKey[:])
	return out
}

// HardcodedKey returns a Key built from the public, hardcoded key bytes.
// Testing only — NOT SECURE.
func HardcodedKey() *Key {
	k, _ := NewKey(hardcodedKey[:])
	return k
}
